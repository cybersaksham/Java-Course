/*
Find value of 7 * 49 / 7 + 35 / 7
 */

package com.company;

public class PS2_5 {
    public static void main(String[] args) {
        int a = 7 * 49 / 7 + 35 / 7;
        System.out.println(a);
        /*
        7 * 49 = 343 and 35 / 7 = 5
        343 / 7 = 49
        49 + 5 = 54
         */
    }
}
