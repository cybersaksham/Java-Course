package com.company;

public class T2_printing_methods {
    public static void main(String[] args) {
        int a = 4;
        float b = 3.14f;

        // Printing Methods
        System.out.println("The value of a is " + a + " & value of b is " + b);
        System.out.printf("The value of a is %d & value of b is %f\n", a, b);
        System.out.format("The value of a is %d & value of b is %f\n", a, b);
    }
}
