package com.company;

public class T6_operators {
    public static void main(String[] args) {
        /* ARITHMETIC OPERATORS
         * a + b --> sum
         * a - b --> subtract
         * a * b --> multiply
         * a / b --> divide
         * a % b --> remainder of (a / b) --> Modulo Operator
         * */

        /* ASSIGNMENT OPERATORS
         * a = b --> Assigning value of b in a
         * a += b --> Add b in a
         * a *= b --> Multiply a by b
         * a -= b --> Subtract b from a
         * a /= b --> Divide a by b
         * */

        /* COMPARISON OPERATORS
         * a == b --> return true if a & b are equal
         * a > b --> return true if a is greater than b
         * a < b --> return true if a is less than b
         * <=, >= also present
         * */

        /* LOGICAL OPERATOR
         * a && b --> return true if a & b both are true
         * a || b --> return true if any one of a & b is true
         * !a --> return reverse of a
         * */

        /* BITWISE OPERATOR
         * a & b --> work on bits of a, b and returns & value
         * a | b --> work on bits of a, b and returns | value
         * Example :
         * 2 & 3                2 | 3
         * 2 --> 10             2 --> 10
         * 3 --> 11             3 --> 11
         * & --> 10             | --> 11
         * Ans --> 10 --> 2     Ans --> 11 --> 3
         * */

        /*
        INCREMENT or DECREMENT OPERATOR
        a++ --> first use a then increase by 1 --> Post-increment
        ++a --> first increase by 1 then use a --> Pre-increment
        a-- --> first use a then decrease by 1 --> Post-decrement
        --a --> first decrease by 1 then use a --> Pre-decrement
         */
    }
}
