package com.company;

public class T4_literals {
    public static void main(String[] args) {
        byte a = 110; // byte has no literal
        int b = 560; // int has no literal
        short c = 87;  // short has no literal
        long d = 454845197416494L; // long has literal L or l
        char e = 'A'; // char is written in single quotes
        float f = 3.14f; // float has literal f
        double g = 3.1428445; // double has literal D but no need to specify
        boolean h = true; // true or false
        String i = "saksham"; // String is written in double quotes

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);
    }
}
